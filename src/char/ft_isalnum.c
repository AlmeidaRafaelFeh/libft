int	ft_isalnum(int nb)
{
	if ((nb >= '0' && nb <= '9')
		|| (nb >= 'a' && nb <= 'z')
		|| (nb >= 'A' && nb <= 'Z'))
	{
		return (1);
	}
	return (0);
}
